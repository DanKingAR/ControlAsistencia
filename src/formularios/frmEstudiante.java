    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if (txtNombres.getText().length() == 0) {
            txtNombres.setForeground(Color.red);
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar los nombred del estudiante");
            txtNombres.requestFocus();
            return;
        }

        if (txtFApellido.getText().length() == 0) {
            txtFApellido.setForeground(Color.red);
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar los apellidos del estudiante");
            txtFApellido.requestFocus();
            return;
        }

        if (txtDni.getText().length() == 0) {
            txtDni.setForeground(Color.red);
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar el dni de estudiante");
            txtDni.requestFocus();
            return;
        }

        if (txtDireccion.getText().length() == 0) {
            txtDireccion.setForeground(Color.red);
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar la direcci√≥n del estudiante");
            txtDireccion.requestFocus();
            return;
        }

        if (txtTelefono.getText().length() == 0) {
            txtTelefono.setForeground(Color.red);
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar un tel√©fono para el estudiante");
            txtTelefono.requestFocus();
            return;
        }

        if (txtSede.getText().length() == 0) {
            txtSede.setForeground(Color.red);
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar la sede para el estudiante");
            txtSede.requestFocus();
            return;
        }

        if (dcfecha_nacimiento.getDate() == null) {
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar la fecha de nacimiento del estudiante");
            dcfecha_nacimiento.requestFocus();
            return;
        }

        if (dcfecha_registro.getDate() == null) {
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar la fecha de registro del estudiante");
            dcfecha_registro.requestFocus();
            return;
        }

        if (cmbGenero.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes seleccionar el g√©nero del estudiante");
            cmbGenero.requestFocus();
            return;
        }

        if (cmbGrado.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes seleccionar el grado del estudiante");
            cmbGrado.requestFocus();
            return;
        }

        if (cmbSeccion.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes seleccionar la secci√≥n del estudiante");
            cmbSeccion.requestFocus();
            return;
        }

        if (cmbJornada.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes seleccionar la jornada del estudiante");
            cmbJornada.requestFocus();
            return;
        }

        dts.setNombre(txtNombres.getText());
        dts.setFapellido(txtFApellido.getText());
        dts.setLapellido(txtLApellido.getText());
        dts.setDni(txtDni.getText());
        dts.setDireccion(txtDireccion.getText());
        dts.setTelefono(txtTelefono.getText());

        int seleccionado = cmbGenero.getSelectedIndex();
        dts.setGenero((String) cmbGenero.getItemAt(seleccionado));
        seleccionado = cmbGrado.getSelectedIndex();
        dts.setGrado((String) cmbGrado.getItemAt(seleccionado));
        seleccionado = cmbSeccion.getSelectedIndex();
        dts.setGrupo((String) cmbSeccion.getItemAt(seleccionado));

        Calendar cal;
        int d, m, a;
        cal = dcfecha_nacimiento.getCalendar();
        d = cal.get(Calendar.DAY_OF_MONTH);
        m = cal.get(Calendar.MONTH);
        a = cal.get(Calendar.YEAR) - 1900;
        dts.setFecha_nace(new Date(a, m, d));

        cal = dcfecha_registro.getCalendar();
        d = cal.get(Calendar.DAY_OF_MONTH);
        m = cal.get(Calendar.MONTH);
        a = cal.get(Calendar.YEAR) - 1900;
        dts.setFecha_registro(new Date(a, m, d));

        seleccionado = cmbJornada.getSelectedIndex();
        dts.setJornada((String) cmbJornada.getItemAt(seleccionado));
        dts.setSede(txtSede.getText());

        if (accion.equals("guardar")) {
            if (func.insertar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "El estudiante fue registrado satisfactoriamente", "Registro del Estudiante", JOptionPane.INFORMATION_MESSAGE);
                mostrar("");
                inhabilitar();
                btnHuella.setEnabled(true);
            }
        } else if (accion.equals("editar")) {
            dts.setIdEstudiante(Integer.parseInt(txtIdAlumno.getText()));

            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "El estudiante fue editado satisfactoriamente", "Registro del Estudiante", JOptionPane.INFORMATION_MESSAGE);
                mostrar("");
                inhabilitar();
                btnHuella.setEnabled(true);
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

