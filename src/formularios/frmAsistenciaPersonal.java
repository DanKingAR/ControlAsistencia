    private void ingreso() {
        ConexionBD postgres = new ConexionBD();
        Connection conn = postgres.conectar();
        String SQL = "SELECT p.idpersonal, p.nombre, p.fapellido, p.lapellido "
                + "FROM personal p LEFT JOIN cargo c ON p.idcargo = c.idcargo "
                + "WHERE dni LIKE '" + txtCodigo.getText() + "' AND estado = 'A' ORDER BY nombre ASC;";

        try {
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery(SQL);
            while (rs.next()) {
                txtIdPersonal.setText(rs.getString("idpersonal"));
                txtNombres.setText(rs.getString("nombre"));
                txtFApellido.setText(rs.getString("fapellido"));
                txtLApellido.setText(rs.getString("lapellido"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
            txtCodigo.requestFocus();
        }

        dts.setIdPersonal(Integer.parseInt(txtIdPersonal.getText()));
        dts.setDni(txtCodigo.getText());        
        dts.setFecha(FechaActual.getText());
        dts.setHora_ingreso(HoraActual.getText());
        dts.setHora_salida(lblHoraSalida.getText());

        String idusuario = PanelInicio.lblidtratabajor.getText();
        dts.setIdusuario(Integer.parseInt(idusuario));

        if (accion.equals("guardar")) {
            if (func.ingreso(dts)) {
                JOptionPane.showMessageDialog(rootPane, "Registro de Asistencia Completo", "Registro de Asistencia Administrativo", JOptionPane.INFORMATION_MESSAGE);
                mostrar("");
                vaciar();
            }
        } 
//        else if (accion.equals("editar")) {
//            dts.setIdAsistenciaAlumnos(Integer.parseInt(txtAsistencia.getText()));
//            if (func.editar(dts)) {
//                JOptionPane.showMessageDialog(rootPane, "Registro de Asistencia Completo", "Registro de Asistencia Administrativo", JOptionPane.INFORMATION_MESSAGE);
//                mostrar("");
//                vaciar();
//            }
//        }
    }

