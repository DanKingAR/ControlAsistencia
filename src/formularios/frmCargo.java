    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if (txtCargo.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes Ingresar un cargo a guardar");
            txtCargo.requestFocus();
            return;
        }

        Cargo dts = new Cargo();
        Cargos func = new Cargos();

        dts.setCargodescripcion(txtCargo.getText().trim());

        if (accion.equals("guardar")) {
            if (func.insertar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "El cargo fue registrado satisfactoriamente");
                mostrar("");
                inhabilitar();
            }
        } else if (accion.equals("editar")) {
            dts.setIdCargo(Integer.parseInt(txtIdCargo.getText().trim()));

            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "El cargo fue editada satisfactoriamente");
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

