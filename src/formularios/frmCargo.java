    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        if (!txtIdCargo.getText().equals("")) {
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "Esta seguro de Eliminar el cargo", "Confirmar", 2);

            if (confirmacion == 0) {
                Cargos func = new Cargos();
                Cargo dts = new Cargo();

                dts.setIdCargo(Integer.parseInt(txtIdCargo.getText().trim()));
                func.eliminar(dts);
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

